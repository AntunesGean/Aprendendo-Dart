void main() {
  // Atribuição
  double a = 10;
  a += 10; // a = a + 10;
  a -= 10; // a = a - 10;
  a *= 10; // a = a * 10;
  a /= 2; // a = a / 10;
  a %= 2; // a = a % 10;

  print(a);

  // Relacionais (==, !=, >, <, >=, <=) - o resultado é sempre um bool
  print(3 == 2);
  print(3 != 2);
  print(3 > 2);
  print(3 < 2);
  print(3 >= 2);
  print(3 <= 2);
}
